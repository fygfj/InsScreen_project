const uint8_t *qw_icon_bitmap(int code, int size, int *actual_size)
{
    const qw_icon_entry_t *entry = qw_icon_find(code);
    if (!entry)
        return NULL;
    if (size > 24) {
        if (actual_size) *actual_size = QW_ICON_LARGE_SIZE;
        return entry->large;
    }
    if (actual_size) *actual_size = QW_ICON_SMALL_SIZE;
    return entry->small;
}
