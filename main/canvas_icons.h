#define CANVAS_BUILTIN_ICON_COUNT \
    ((int)(sizeof(CANVAS_BUILTIN_ICONS) / sizeof(CANVAS_BUILTIN_ICONS[0])))

/**
 * 按名称查找内置图标，返回 32 字节位图指针；未找到返回 NULL。
 */
static inline const uint8_t *canvas_find_builtin_icon(const char *name)
{
    if (!name) return NULL;
    for (int i = 0; i < CANVAS_BUILTIN_ICON_COUNT; i++) {
        if (strcmp(CANVAS_BUILTIN_ICONS[i].name, name) == 0)
            return CANVAS_BUILTIN_ICONS[i].bmp;
    }
    return NULL;
}
